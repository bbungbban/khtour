package ksmart42.khtour.dto;

public class Exhibition {
	
	private String specialExhibCode;
	private String chMusCode;
	private String specialExhibCate;
	private String specialExhibName;
	private String specialExhibStart;
	private String specialExhibEnd;
	private String specialExhibCon;
	public String getSpecialExhibCode() {
		return specialExhibCode;
	}
	public void setSpecialExhibCode(String specialExhibCode) {
		this.specialExhibCode = specialExhibCode;
	}
	public String getChMusCode() {
		return chMusCode;
	}
	public void setChMusCode(String chMusCode) {
		this.chMusCode = chMusCode;
	}
	public String getSpecialExhibCate() {
		return specialExhibCate;
	}
	public void setSpecialExhibCate(String specialExhibCate) {
		this.specialExhibCate = specialExhibCate;
	}
	public String getSpecialExhibName() {
		return specialExhibName;
	}
	public void setSpecialExhibName(String specialExhibName) {
		this.specialExhibName = specialExhibName;
	}
	public String getSpecialExhibStart() {
		return specialExhibStart;
	}
	public void setSpecialExhibStart(String specialExhibStart) {
		this.specialExhibStart = specialExhibStart;
	}
	public String getSpecialExhibEnd() {
		return specialExhibEnd;
	}
	public void setSpecialExhibEnd(String specialExhibEnd) {
		this.specialExhibEnd = specialExhibEnd;
	}
	public String getSpecialExhibCon() {
		return specialExhibCon;
	}
	public void setSpecialExhibCon(String specialExhibCon) {
		this.specialExhibCon = specialExhibCon;
	}
	@Override
	public String toString() {
		return "Exhibition [specialExhibCode=" + specialExhibCode + ", chMusCode=" + chMusCode + ", specialExhibCate="
				+ specialExhibCate + ", specialExhibName=" + specialExhibName + ", specialExhibStart="
				+ specialExhibStart + ", specialExhibEnd=" + specialExhibEnd + ", specialExhibCon=" + specialExhibCon
				+ "]";
	}
	
	
	
	
}
